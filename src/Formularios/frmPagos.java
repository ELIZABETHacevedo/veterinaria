    private double minimo()
    {
       double min=0;
       if(this.lRel.size()>0)
       {
           min=this.lRel.get(0).getoConsulta().getMonto();
           for(int i=1; i<this.lRel.size(); i++)
           {
               if(min>this.lRel.get(i).getoConsulta().getMonto())
                   min=this.lRel.get(i).getoConsulta().getMonto();
           }
       }
       return min;
    }
    
    private double maximo()
    {
        
       double max=0;
       if(this.lRel.size()>0)
       {
           max=this.lRel.get(0).getoConsulta().getMonto();
           for(int i=1; i<this.lRel.size(); i++)
           {
               if(max<this.lRel.get(i).getoConsulta().getMonto())
                   max=this.lRel.get(i).getoConsulta().getMonto();
           }
       }
       return max;
    }
    
    private double promedio()
    {
        double prom=(this.sumatoria()/this.lRel.size());
        prom*=100;
        prom= Math.round(prom);
        return (prom/100);
    }
    
    private double sumatoria()
    {
        double total=0;
        for(int i=0; i<this.lRel.size(); i++)
        {
             total+= this.lRel.get(i).getoConsulta().getMonto();
        }
        total*=100;
        total= Math.round(total);
        return (total/100);
    }
    
   
    
