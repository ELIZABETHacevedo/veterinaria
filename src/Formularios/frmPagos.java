    private void cargaTabla()
    {     
        this.mdlTabla.setRowCount(0);
        for(int j=0; j<this.lRel.size(); j++)
        {
            String[] registro = { 
                
                this.lRel.get(j).getDuennio().getNombre(),
                this.lRel.get(j).getoMascota().getNombre(),
                this.lRel.get(j).getoConsulta().getNumC(),
                Double.toString(this.lRel.get(j).getoConsulta().getMonto()),
                this.lRel.get(j).getVeterinario().getNombre()+ " " +this.lRel.get(j).getVeterinario().getApellido(),
                this.lRel.get(j).getoSucursal().getNombre(),
                   this.sdfe.format( this.lRel.get(j).getoConsulta().getFechaHora())
            };
            this.mdlTabla.addRow(registro);
        }
    }

